#ifndef BSEmotionConst_h
#define BSEmotionConst_h

// 小表情或emoji的最大行数
#define kEmotionMaxRows 3
// 小表情或emoji的最大列数
#define kEmotionMaxCols 7
// 小表情或emoji，每页最多显示多少个表情
#define kEmotionMaxCountPerPage (kEmotionMaxRows * kEmotionMaxCols - 1)

// 大表情的最大行数
#define kLargeEmotionMaxRows 2
// 大表情的最大列数
#define kLargeEmotionMaxCols 4
// 大表情，每页最多显示多少个表情
#define kLargeEmotionMaxCountPerPage (kLargeEmotionMaxRows * kLargeEmotionMaxCols)


#endif /* BSEmotionConst_h */
